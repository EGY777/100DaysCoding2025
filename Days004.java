
package days004;

public class Days004 {

    public static void main(String[] args) {
        // kode (\t) menambah spasi
        System.out.println("nama :\tEgy");
        //kode (\\) menambah garis miring terbalik pada ouputan
        System.out.println("contoh : \\");
        // kode (\n) memindahkan teks ke garis baru
        System.out.println("contohnya seperti ini \n hay");
        //kode (\r) menghilangkan teks sebelumnya
        System.out.println("contoh \rhay");
        
    }
    
}
