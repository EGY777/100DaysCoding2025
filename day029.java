public class Perbandingan {
    public static void main(String[] args) {
      
        int x = 50;
        int y = 60;
      
        //MENGGUNAKAN OPERATOR <
        System.out.println("x < y : " + (x < y));  // true
        //MENGGUNAKAN OPERATOR >
        System.out.println("x > y : " + (x > y));  // false
    }
}
