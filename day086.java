package king;
public class King {
    public static void main(String[] args) {
        int [] angka = {10,30,20,1,80,50};
        int minimum = angka[0];
        for (int i = 0; i < angka.length; i++) {
            if (angka[i] < max) {
                minimum = angka[i];
            }
        }
        System.out.println("Nilai minimum : " + max);
    }
}
